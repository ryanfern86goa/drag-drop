define('plugins/Bookstrap.Navigation.SwipeNew',[
  'underscore',
  'Bookstrap.DOMPage'
  ],
  function(_, BookstrapDOMPage) {
    var POSITION_PREVIOUS = 0,
        POSITION_CURRENT = 1,
        POSITION_NEXT = 2;

    var BookstrapSwipe = function() {
      this._preventUserInput = false;
      _.bindAll(this);

            bookstrap.viewportContainer.$el.hammer({
                drag_lock_to_axis: true,
                show_touches: true
            }).on("release dragleft dragright swipeleft swiperight", this.handleHammer);

            bookstrap.viewport.$el.on('webkitTransitionEnd otransitionend oTransitionEnd msTransitionEnd transitionend', this.onAnimationEnd);

            bookstrap.on('ready', this.init);
            bookstrap.on('resize', this.resizeContainers);
      bookstrap.moveToDOMPage = this.moveToDOMPage;
      bookstrap.updateViewPortSize = this.updateViewPortSize;
        };

        BookstrapSwipe.prototype.init = function() {
      if (bookstrap.domPages.getLength() !== 3) {
        console.error('3 DOM Pages expected');
        return;
      }

      this.container = bookstrap.viewport.$el;
      this.domPages = $("div.column", this.container);

      this.pane_width = bookstrap.viewportContainer.$el.width();
      this.pane_count = 3;

      this.currentPageIndex = bookstrap.domPages.pages.indexOf(bookstrap.domPages.current.page);
      bookstrap.updateViewPortSize();
      bookstrap.resizeViewPort();
      this.resizeContainers();
      this.enableUserInput();
        };

        BookstrapSwipe.prototype.handleHammer = function(event) {
      if (this._preventUserInput  || bookstrap.disablePageSwipe)
        return;

      if (event.gesture.touches.length > 1)
        return;

            var self = this;

            event.gesture.preventDefault();

            switch(event.type) {
        case 'dragright':
        case 'dragleft':
          // stick to the finger
          var pane_offset = -(100/this.pane_count);
          var drag_offset = ((100/this.pane_width) * event.gesture.deltaX) / this.pane_count;

          // slow down at the first and last pane
          if( (!bookstrap.hasPrevious() && event.gesture.direction == Hammer.DIRECTION_RIGHT)
              || (!bookstrap.hasNext() && event.gesture.direction == Hammer.DIRECTION_LEFT) ) {
              drag_offset *= .4;
          }

          this.setContainerOffset(pane_offset + drag_offset);
          break;

        case 'swipeleft':
          this.disableUserInput();
          if (bookstrap.hasNext()) {
            self.nextPage();
          } else {
            self.currentPage();
          }
          event.gesture.stopDetect();
          break;

        case 'swiperight':
          this.disableUserInput();
            if (bookstrap.hasPrevious()){
              self.previousPage();
            } else {
                self.currentPage();
            }
          event.gesture.stopDetect();
          break;

        case 'release':
          if (! $(event.target).is('.book-spread-page'))
            return;
          // navigate to neighbour if moved >1/3 screen width
          if(Math.abs(event.gesture.deltaX) > this.pane_width/3) {
            if (bookstrap.hasPrevious() && event.gesture.direction == Hammer.DIRECTION_RIGHT) {
              self.previousPage();
            } else if (bookstrap.hasNext() && event.gesture.direction == Hammer.DIRECTION_LEFT) {
              self.nextPage();
            } else {
                // snap back to current page
                self.currentPage();
            }
          } else {
            // snap back to current page
            self.currentPage();
          }
          break;
        }
        };

        BookstrapSwipe.prototype.onAnimationEnd = function(event) {
      this.enableUserInput();
      this.updatePagePositions();
        }

    BookstrapSwipe.prototype.updateViewPortSize = function() {
      bookstrap.viewport.width = document.documentElement.clientWidth * this.pane_count;
      bookstrap.viewport.height = document.documentElement.clientHeight;
      bookstrap.viewportContainer.width = document.documentElement.clientWidth;
      bookstrap.viewportContainer.height = document.documentElement.clientHeight;
    };

    BookstrapSwipe.prototype.resizeContainers = function() {
      this.resizePages();
      this.updatePagePositions();
    }

    BookstrapSwipe.prototype.resizePages = function() {
      var self = this;
      self.pane_width = bookstrap.viewportContainer.width;
      bookstrap.domPages.forEach(function(domPage){
        domPage.$el.css({
          width: bookstrap.viewportContainer.width,
          height: bookstrap.viewportContainer.height
        });
      });
    };

    BookstrapSwipe.prototype.moveToDOMPage = function(domPage) {
      this.currentPageIndex = bookstrap.domPages.pages.indexOf(domPage.page);
      this.updatePagePositions();
    };

    BookstrapSwipe.prototype.previousPage = function() {
      this.currentPageIndex = this.currentPageIndex - 1;
      bookstrap.domPages.setCurrentPage(bookstrap.domPages.pages.get(this.currentPageIndex));
      this.setContainerOffsetByIndex(POSITION_PREVIOUS, true);
    };
    BookstrapSwipe.prototype.currentPage = function() {
      this.setContainerOffsetByIndex(POSITION_CURRENT, true);
    };
    BookstrapSwipe.prototype.nextPage = function() {
      this.currentPageIndex = this.currentPageIndex + 1;
      bookstrap.domPages.setCurrentPage(bookstrap.domPages.pages.get(this.currentPageIndex));
      this.setContainerOffsetByIndex(POSITION_NEXT, true);
    };

    BookstrapSwipe.prototype.setContainerOffsetByIndex = function(index, animate) {
      var percentOffset = -((100/(this.pane_count)) * index);
      this.setContainerOffset(percentOffset, animate);
    };

    BookstrapSwipe.prototype.setContainerOffset = function(percent, animate) {
            this.container.removeClass("animate");
      if (animate) {
          this.container.addClass("animate");
      }
            this.container.css("-webkit-transform", "translate3d("+ percent +"%,0,0) scale3d(1,1,1)");
    };

    BookstrapSwipe.prototype.updatePagePositions = function(){
      var self = this;
      bookstrap.domPages.forEach(function(domPage){
        //if (!domPage.page) return;
        var index = domPage.position + 1;
        domPage.$el.css({
          top: 0,
          left: (index * self.pane_width)
        });
      });
      this.setContainerOffsetByIndex(POSITION_CURRENT, false);
    };
    BookstrapSwipe.prototype.enableUserInput = function(viewport) {
      this._preventUserInput = false;
    };

    BookstrapSwipe.prototype.disableUserInput = function(viewport) {
      this._preventUserInput = true;
    };

    return BookstrapSwipe;
});
